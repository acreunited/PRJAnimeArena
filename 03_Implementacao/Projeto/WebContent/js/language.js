function diffPages(lan, page) {
	navBarFooter(lan, page);
	if (page == "index") {
		announcements(lan);
	}
	else if (page == "leaderboards") {
		leaderboards(lan);
	}
	else if (page=="missions") {
		missions(lan);
	}
	else if (page=="help") {
		help(lan);
	}
	else if (page=="helpCharacters") {
		helpCharacters(lan);
	}
	else if (page=="characterInfo") {
		characterInfo(lan);
	}
	else if (page=="changeAvatar") {
		changeAvatar(lan);
	}
	else if (page=="changeMail") {
		changeMail(lan);
	}
	else if (page=="changePassword") {
		changePassword(lan);
	}
	else if (page=="changeBack") {
		changeBack(lan);
	}
}

function setLanguage(lan, page) {
	
	const xhttp = new XMLHttpRequest();

	xhttp.onload = function() {
		
		if (xhttp.status === 200 && xhttp.readyState === 4) {
			diffPages(lan, page);
		} 
	}

	xhttp.open("POST", "Language?action="+lan, true);
	xhttp.send(null);
}

