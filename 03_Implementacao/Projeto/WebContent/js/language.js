function specialChars(string) {
	return string.replaceAll("Ú", "\u00DA").replaceAll("ã", "\u00E3").replaceAll("ú", "\u00FA").replaceAll("Ç", "\u00C7")
	.replaceAll("õ", "\u00F5").replaceAll("Õ", "\u00D5").replaceAll("É", "\u00C9").replaceAll("Ã", "\u00C3")
	.replaceAll("é", "\u00E9").replaceAll("à", "\u00E0").replaceAll("ê", "\u00EA").replaceAll("í", "\u00ED")
	.replaceAll("á", "\u00E1").replaceAll("õ", "\u00F5").replaceAll("ó", "\u00F3").replaceAll("ç", "\u00E7")
	.replaceAll("è", "\u00E8").replaceAll("Á", "\u00C1");
}

